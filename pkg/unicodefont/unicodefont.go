func init() {
	bothandler.RegisterCatchallHandler(UnicodeFontReplace)

	s := strings.Split(fontmapSrc, "\n")
	for k, line := range s {
		l := utf8.RuneCountInString(line)
		if l != 26 {
			log.Println("line is not 26 chars long:", k, l)
			continue
		}
		fontmap = append(fontmap, line)
	}
}

// unicodeReplace replaces the characters in i with the characters in to
// the fontmap. It returns the new string. If a character is not in the
// fontmap, it is left as is.
func unicodeReplace(to int, i string) string {
	out := ""
	for _, c := range i {
		k := int(c - 'a')
		if k >= 0 && k < len(fontmap[to]) {
			out += string([]rune(fontmap[to])[k])
		} else {
			out += string(c)
		}
	}
	origout := out
	t2s := gojianfan.T2S(out)
	s2t := gojianfan.S2T(out)
	if origout != t2s {
		out = t2s
	} else if origout != s2t {
		out = s2t
	}

	return out
}

func UnicodeFontReplace(request bothandler.Request) string {
	if !strings.HasPrefix(request.Content, "!unicode ") {
		return ""
	}

	i, _ := strings.CutPrefix(request.Content, "!unicode ")
	i = strings.ToLower(i)

	to := rand.Intn(len(fontmap))
	return unicodeReplace(to, i)
}
